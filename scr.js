//#region for drowp down
// Get the button and the dropdown window
var button = document.getElementById("bt_drop");
var dropdown = document.getElementById("drop_down");

// Add a click event listener to the button
button.onclick = function() {
  // Toggle the display of the dropdown window
  if (dropdown.style.display === "block") {
    dropdown.style.display = "none";
  } else {
    // Position the dropdown below the button
    dropdown.style.display = "block";
    var rect = button.getBoundingClientRect();
    dropdown.style.top = rect.bottom + "px";
    dropdown.style.left = rect.left + "px";
  }
}

// Close the dropdown if clicked outside
window.onclick = function(event) {
  if (event.target != button && !dropdown.contains(event.target)) {
    dropdown.style.display = "none";
  }
}

